package Model;

/**
 *
 * @author pc
 */
public class Wish {
    private int productID;
    private int UserID;

    public Wish() {
    }

    public Wish(int productID, int UserID) {
        this.productID = productID;
        this.UserID = UserID;
    }

    public int getProductID() {
        return productID;
    }

    public void setProductID(int productID) {
        this.productID = productID;
    }

    public int getUserID() {
        return UserID;
    }

    public void setUserID(int UserID) {
        this.UserID = UserID;
    }
    
}
